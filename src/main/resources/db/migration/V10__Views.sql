CREATE view "P0000ProjMembersView" ("P0000ProjectId", "A0000UserId", "Nickname", "UserName", "Gender", "FullName") as
SELECT OLINGO."P0000ProjMembers"."P0000ProjectId",
       OLINGO."P0000ProjMembers"."A0000UserId",
       DEV_GREENTRAK00.A0000USERS."Nickname",
       DEV_GREENTRAK00.A0000USERS."UserName",
       DEV_GREENTRAK00.A0000USERS."Gender",
       DEV_GREENTRAK00.A0000USERS."FullName"
FROM OLINGO."P0000ProjMembers",
     DEV_GREENTRAK00.A0000USERS
WHERE OLINGO."P0000ProjMembers"."A0000UserId" = DEV_GREENTRAK00.A0000USERS.ID;